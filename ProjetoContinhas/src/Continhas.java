
public class Continhas {
	public static void main(String[] args) {
		
		int a;
		int b,c;
		
		a = 27;
		b = 3;
		c = a+b;
		
		System.out.println("O valor de C vale: " + c);
		
		c = a - b;
		
		System.out.println("O valor de C vale: " + c);
		
		c = a * b;
		
		System.out.println("O valor de C vale: " + c);
		
		c = a / b;
		
		System.out.println("O valor de C vale: " + c);
		
		c = a % b; /* é o resultado do quociente da divisão 27 / 3 */
		
		System.out.println("O valor de C vale: " + c);
	
		double d;
		
		d = 1/2; /* vai dar o valor 0.0 */
		
		System.out.println("O valor de d vale: " + d);
		
		d = 1.0/2; /* vai dar o valor 0.5 */
		
		System.out.println("O valor de d vale: " + d);
		
		d = 1.0/3; /* vai dar o valor 0.333333.... */
		
		System.out.println("O valor de d vale: " + d);
	
	
		System.out.println();
	}
}
